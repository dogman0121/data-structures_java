package lab3;

public class Task2 {
    public static void main(String[] args) {
        Double a = Double.valueOf(6546.546);
        System.out.println(Double.parseDouble("13.45456"));
        System.out.println("Byte: " + a.byteValue());
        System.out.println("Short: " + a.shortValue());
        System.out.println("Integer: " + a.intValue());
        System.out.println("Long: " + a.longValue());
        System.out.println("Float: " + a.floatValue());
        String s = Double.toString(34.78);
        System.out.println(s);
    }
}
